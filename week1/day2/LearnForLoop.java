package week1.day1;

public class LearnForLoop {

	public static void main(String[] args) {
		
		//print the numbers from 1 to 5
		//type for and do ctrl+space
		
		for (int i = 1; i <= 5; ++i) {
			System.out.println(i);
		}
		
		System.out.println("--------------------------");
		
		for (int i = 5; i >=1; i--) {
			System.out.println(i);
		}
	}

}
